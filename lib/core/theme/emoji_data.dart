class EmojiData {
  static final Map<String, String> _emojiMap = _generateMap();

  static Map<String, String> _generateMap() {
    final map = <String, String>{};
    for (int i = 0; i < data.length; i++) {
      for (int j = 0; j < data[i].length; j++) {
        final emoji = data[i][j];
        final path = "assets/emoji/${i}_$j.png";
        map[emoji] = path;
        final normalized = emoji.replaceAll('\uFE0F', '');
        if (normalized != emoji) {
          map.putIfAbsent(normalized, () => path);
        }
      }
    }
    return map;
  }

  static String? getEmojiPath(String emoji) {
    if (_emojiMap.containsKey(emoji)) return _emojiMap[emoji];
    final normalized = emoji.replaceAll('\uFE0F', '');
    return _emojiMap[normalized];
  }

